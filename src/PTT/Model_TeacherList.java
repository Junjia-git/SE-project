package PTT;

public class Model_TeacherList {
	
	private String lecturerID;
	private String lecturerName;
	private String degree;
	private String mainCourse;
	private String otherCourseTeaching;
	
	

	public String getLecturerID() {
		return lecturerID;
	}

	public void setLecturerID(String lecturerID) {
		this.lecturerID = lecturerID;
	}

	public String getLecturerName() {
		return lecturerName;
	}

	public void setLecturerName(String lecturerName) {
		this.lecturerName = lecturerName;
	}

	public String getDegree() {
		return degree;
	}

	public void setDegree(String degree) {
		this.degree = degree;
	}

	public String getMainCourse() {
		return mainCourse;
	}

	public void setMainCourse(String mainCourse) {
		this.mainCourse = mainCourse;
	}

	public String getOtherCourseTeaching() {
		return otherCourseTeaching;
	}

	public void setOtherCourseTeaching(String otherCourseTeaching) {
		this.otherCourseTeaching = otherCourseTeaching;
	}
	
	

}

	